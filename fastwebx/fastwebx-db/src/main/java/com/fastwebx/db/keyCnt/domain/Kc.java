package com.fastwebx.db.keyCnt.domain;

import com.fastwebx.db.keyCnt.inf.IKc;

/**
 * @author mj
 */
public class Kc<T> implements IKc<T> {
	private T key;
	private Integer cnt;
	public T getKey() {
		return key;
	}
	public void setKey(T key) {
		this.key = key;
	}
	public Integer getCnt() {
		return cnt;
	}
	public void setCnt(Integer cnt) {
		this.cnt = cnt;
	}
	public Kc(T key, Integer cnt) {
		super();
		this.key = key;
		this.cnt = cnt;
	}
	
}
