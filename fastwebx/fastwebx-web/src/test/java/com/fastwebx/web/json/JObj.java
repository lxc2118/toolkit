package com.fastwebx.web.json;

import java.util.Date;

/**
 * @author mj
 */
public class JObj {
	
	public JObj(){
		
	}
	
	public JObj(String aaa,int bbb){
		this.aaa = aaa;
		this.bbb=bbb;
	}
	private String aaa;
	private int bbb;
	private Date ccc;
	public String getAaa() {
		return aaa;
	}
	public void setAaa(String aaa) {
		this.aaa = aaa;
	}
	public int getBbb() {
		return bbb;
	}
	public void setBbb(int bbb) {
		this.bbb = bbb;
	}
	public Date getCcc() {
		return ccc;
	}
	public void setCcc(Date ccc) {
		this.ccc = ccc; 
	}
	
}
