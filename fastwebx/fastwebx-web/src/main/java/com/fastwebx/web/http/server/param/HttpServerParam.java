package com.fastwebx.web.http.server.param;
/**
 * @author mj
 */
public class HttpServerParam {
	private String json;
	private Long time;
	private String sign;
	public String getJson() {
		return json;
	}
	public void setJson(String json) {
		this.json = json;
	}
	public Long getTime() {
		return time;
	}
	public void setTime(Long time) {
		this.time = time;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	
}
