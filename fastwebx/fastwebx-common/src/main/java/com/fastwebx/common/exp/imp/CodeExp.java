package com.fastwebx.common.exp.imp;

import com.fastwebx.common.exp.base.ErrorCode;

/**
 * @author mj
 */
public class CodeExp extends ErrorCode{
	private String code;
	private String msg;
	@Override
	public String acqCode() {
		
		return code;
	}
	@Override
	public String getMessage() {
		
		return msg;
	}
	public CodeExp(String code, String msg) {
		super();
		this.code = code;
		this.msg = msg;
	}
	
	public CodeExp(String code){
		this.code=code;
		this.msg=code;
	}
	
	
}
